#Desenvolva um programa que leia as duas notas de um aluno calcule a e mostre sua media
n1=int(input(' Informe a primeira nota obtida: '))
n2=int(input(' Informe a segunda nota obtida: '))
print(' A soma das notas informadas é {} \n A média das notas informadas é {}'.format(n1+n2, (n1+n2)/2))
